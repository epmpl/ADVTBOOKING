    [Ajax.AjaxMethod]
    public DataSet bindAgency(string compcode, string agency)
    {
        DataSet ds = new DataSet();
        if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "sql")
        {
            NewAdbooking.Classes.agencynew logindetail = new NewAdbooking.Classes.agencynew();
            ds = logindetail.bindagency(compcode, agency);
        }
        else if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "orcl")
        {
            NewAdbooking.classesoracle.agencynew logindetail = new NewAdbooking.classesoracle.agencynew();
            ds = logindetail.bindagency(compcode, agency);
        }

        return ds;
    }

