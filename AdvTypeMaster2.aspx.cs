    [Ajax.AjaxMethod(Ajax.HttpSessionStateRequirement.Read)]
    public DataSet chkzonecodename(string str)
    {
         DataSet ds = new DataSet();
        if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "sql")
        {
        NewAdbooking.Classes.AdvTypeMaster2 chk = new NewAdbooking.Classes.AdvTypeMaster2();
       
        ds = chk.countzonecode(str,Session["compcode"].ToString());
        return ds;

        }
        if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "orcl")
        {
            NewAdbooking.classesoracle.AdvTypeMaster2 chk = new NewAdbooking.classesoracle.AdvTypeMaster2();
            ds = chk.countzonecode(str, Session["compcode"].ToString());
            return ds;
        }
        else if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "mysql")
           {
               string procedureName = "ADVERTISEMENT_CODENAME_ADVERTISEMENT_CODENAME_P";
               NewAdbooking.classmysql.CommonClass obj = new NewAdbooking.classmysql.CommonClass();
               string[] parameterValueArray = { str, Session["compcode"].ToString(),""};
               ds = obj.DynamicBindProcedure(procedureName, parameterValueArray);
           }
        return ds;
    }

