    //		public void addPub()
    public void addPubCent()
    {
        NewAdbooking.Classes.BookingCenterMaster PubName = new NewAdbooking.Classes.BookingCenterMaster();
        DataSet ds = new DataSet();
        ds = PubName.Pubcent();
        ListItem li1;
        li1 = new ListItem();
        li1.Text = "---Select Publication Center---";
        li1.Value = "0";
        li1.Selected = true;
        drpubcentname.Items.Add(li1);

        int i;
        for (i = 0; i < ds.Tables[0].Rows.Count; i++)
        {
            ListItem li;
            li = new ListItem();
            li.Value = ds.Tables[0].Rows[i].ItemArray[1].ToString();
            li.Text = ds.Tables[0].Rows[i].ItemArray[0].ToString();
            drpubcentname.Items.Add(li);
        }
    }


