            ds = check.checknoisscode(noisscode, txtnoissuedayname,compcode, userid);
            return ds;
        }
        else if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "orcl")
        {
            NewAdbooking.classesoracle.NoIssueDayMaster check = new NewAdbooking.classesoracle.NoIssueDayMaster();

            ds = check.checknoisscode(noisscode,txtnoissuedayname,compcode, userid);
            return ds;
