    public DataSet MainEditionBind()
    {
        drpedition.Items.Clear();
        DataSet ds = new DataSet();
        if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "sql")
        {

            NewAdbooking.Classes.EditionWise_RatioEntry PMst = new NewAdbooking.Classes.EditionWise_RatioEntry();
            ds = PMst.bindedition(hiddencompcode.Value);
        }

        else if (ConfigurationSettings.AppSettings["ConnectionType"].ToString() == "orcl")
        {
             NewAdbooking.classesoracle.EditionWise_RatioEntry PMst = new NewAdbooking.classesoracle.EditionWise_RatioEntry();
             ds = PMst.bindedition(hiddencompcode.Value);
        }
        else
        {
           // NewAdbooking.classmysql.EditionWise_RatioEntry PMst = new NewAdbooking.classmysql.EditionWise_RatioEntry();
           // ds = PMst.addlang(hiddencompcode.Value);
        }
        int i;
        ListItem li1;
        li1 = new ListItem();
        li1.Text = "----Select Edition----";
        li1.Value = "0";
        li1.Selected = true;
        drpedition.Items.Add(li1);

        if (ds.Tables.Count > 0)
        {
            for (i = 0; i < ds.Tables[0].Rows.Count; i++)
            {
                ListItem li;
                li = new ListItem();
                li.Text = ds.Tables[0].Rows[i].ItemArray[1].ToString();
                li.Value = ds.Tables[0].Rows[i].ItemArray[0].ToString();
                drpedition.Items.Add(li);
            }
        }

        return ds;
    }

